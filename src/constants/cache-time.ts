/**
 * TanStack Query のキャッシュ時間定数
 *
 * staleTime: データが「古い」とみなされるまでの時間
 * - この時間内は再フェッチせずにキャッシュを使用
 * - データの更新頻度に応じて適切な値を設定
 */
export const CACHE_TIME = {
  /** 1分 - 頻繁に変わるデータ（検索結果、統計情報など） */
  SHORT: 1000 * 60,

  /** 5分 - やや変わるデータ（ユーザープロフィール、お気に入りなど） */
  MEDIUM: 1000 * 60 * 5,

  /** 30分 - 滅多に変わらないデータ（書籍詳細、ジャンル一覧など） */
  LONG: 1000 * 60 * 30,

  /** 無限 - 不変のデータ（書籍の目次・内容など） */
  FOREVER: Infinity,
} as const;
